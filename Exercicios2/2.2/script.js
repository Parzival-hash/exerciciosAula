// Faça um Programa que peça a base e
// altura de um quadrado, em seguida
// mostre o dobro desta área para o
// usuário.

let base = parseInt(prompt("Insira o valor da base em CM"))
let altura = parseInt(prompt("Insira o valor da altura em CM"))

let area = (base * altura) /2
let dobro = area * 2
alert("O valor da area do quadrado é de " +area +"CM e o dobro desta area é de" +dobro +"CM.")
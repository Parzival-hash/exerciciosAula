// Faça um algoritmo para ler dois números inteiros e
// informar se estes são iguais ou diferentes.


let numero1 = parseInt(prompt("insira o valor de A: "))
let numero2 = parseInt(prompt("insira o valor de B: "))

if(numero1 == numero2){
    alert("Os numeros digitados são iguais")
}else{
    alert("Os numeros digitados são diferentes")
}